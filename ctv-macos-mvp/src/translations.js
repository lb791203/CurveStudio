export function setLanguage(lang) {
  if (lang === "en" || lang === "zh") {
    currentLang = lang;
  }
}

export function t(key, fallback = "") {
  return TRANSLATIONS[currentLang]?.[key] || TRANSLATIONS["zh"]?.[key] || fallback || key;
}

export function updateDomTranslations(lang = currentLang) {
  setLanguage(lang);
  const elements = document.querySelectorAll("[data-translate]");
  elements.forEach((el) => {
    const key = el.dataset.translate;
    if (key && TRANSLATIONS[currentLang]?.[key]) {
      // For input placeholders
      if (el.tagName === "INPUT" && el.placeholder) {
        el.placeholder = TRANSLATIONS[currentLang][key];
      } else {
        // Keep icons (only replace the text portion if we want, but since data-translate is on the text node or target node we can replace children/text node)
        // If there's an icon span, we should be careful not to destroy it. Let's only replace text nodes, or check if it has children.
        const iconSpan = el.querySelector(".nav-icon, .theme-icon, .theme-toggle-btn span");
        if (iconSpan) {
          // Keep the icon, only replace the text content of the element or sibling text node
          const textNode = Array.from(el.childNodes).find(n => n.nodeType === Node.TEXT_NODE);
          if (textNode) {
            textNode.textContent = TRANSLATIONS[currentLang][key];
          }
        } else {
          el.textContent = TRANSLATIONS[currentLang][key];
        }
      }
    }
  });

  const exactDictionary = currentLang === "en" ? STATIC_TEXT_ZH_EN : STATIC_TEXT_EN_ZH;
  translateExactTextNodes(document.body, exactDictionary);
  translateExactAttributes(document.body, exactDictionary);
  translateSelectOptions(document.body, exactDictionary);

  // Handle document title
  document.title = currentLang === "en" ? "CTV Curve Utility" : "CTV 曲线工具";
}

function translateExactTextNodes(root, dictionary) {
  if (!root || !dictionary) return;
  const showText = globalThis.NodeFilter?.SHOW_TEXT ?? 4;
  const walker = document.createTreeWalker(root, showText);
  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);
  for (const node of nodes) {
    if (shouldSkipExactTranslation(node)) continue;
    const original = node.nodeValue || "";
    const trimmed = original.trim();
    if (!trimmed || !dictionary[trimmed]) continue;
    node.nodeValue = original.replace(trimmed, dictionary[trimmed]);
  }
}

function shouldSkipExactTranslation(node) {
  const parent = node.parentElement;
  if (!parent) return true;
  if (parent.closest("svg, textarea, input, select, option, tbody, .patch-preview-grid, .patch-detail-table, .icc-preview-grid")) {
    return true;
  }
  return false;
}

function translateExactAttributes(root, dictionary) {
  if (!root || !dictionary) return;
  const elements = root.querySelectorAll("[aria-label], [title], [placeholder]");
  elements.forEach((el) => {
    ["aria-label", "title", "placeholder"].forEach((attr) => {
      const value = el.getAttribute(attr);
      if (value && dictionary[value]) el.setAttribute(attr, dictionary[value]);
    });
  });
}

function translateSelectOptions(root, dictionary) {
  if (!root || !dictionary) return;
  root.querySelectorAll("option").forEach((option) => {
    const value = option.textContent?.trim();
    if (value && dictionary[value]) option.textContent = dictionary[value];
  });
}
