function translateDynamicZhText(text) {
  if (!text) return text;
  if (STATIC_TEXT_ZH_EN[text]) return STATIC_TEXT_ZH_EN[text];
  let output = text;
  const dynamicEntries = Object.entries(STATIC_TEXT_ZH_EN)
    .filter(([source]) => /[\u4e00-\u9fff]/.test(source) && source.length >= 4)
    .sort((a, b) => b[0].length - a[0].length);
  for (const [source, target] of dynamicEntries) {
    if (output.includes(source)) output = output.split(source).join(target);
  }
  output = output
    .replace(/(\d+)\s*个色块/g, "$1 patches")
    .replace(/(\d+)\s*色块/g, "$1 patches")
    .replace(/(\d+)\s*列 x (\d+)\s*行/g, "$1 columns x $2 rows")
    .replace(/减少/g, "Reduce")
    .replace(/增加/g, "Increase")
    .replace(/不变/g, "Keep")
    .replace(/全部/g, "All")
    .replace(/当前/g, "Current")
    .replace(/目标Tone/g, "Target Tone")
    .replace(/实测Tone/g, "Measured Tone")
    .replace(/MeasuredTone/g, "Measured Tone")
    .replace(/套用后/g, "After Simulation")
    .replace(/改善/g, "Improved")
    .replace(/变差/g, "Worsened")
    .replace(/说明/g, "Note")
    .replace(/可模拟/g, "Simulatable")
    .replace(/平均/g, "Average")
    .replace(/状态/g, "Status")
    .replace(/校验/g, "Verification")
    .replace(/对比/g, "Comparison")
    .replace(/阶调/g, "Tone")
    .replace(/测量/g, "Measurement")
    .replace(/曲线/g, "Curve")
    .replace(/模拟/g, "Simulation")
    .replace(/点/g, "points")
    .replace(/个/g, "")
    .replace(/列/g, "columns")
    .replace(/行/g, "rows");
  return output;
}
